package model;

import db.Mere;

/**
 *
 * @author amboa
 */
public class Ingredient extends Mere{
    String id;
    String nom;

    public Ingredient() {
    }

    public Ingredient(String id, String nom) {
        this.id = id;
        this.nom = nom;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }
    
}
